package decoratorpattern;

public abstract class BeeBase {
    
    private int strength;
    private int moveSpeed;
    private int harvestSpeed;
    
    public int getStrength() {
        return strength;
    }
    
    public int getMoveSpeed() {
        return moveSpeed;
    }
    
    public int getHarvestSpeed() {
        return harvestSpeed;
    }

}
